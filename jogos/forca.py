import random

def jogar():

    imprime_boas_vindas()
    palavra_secreta = seleciona_palavra_secreta("frutas.txt")

    letras_acertadas = inicializa_palavras_acertadas(palavra_secreta)

    enforcou = False
    acertou = False
    erros = 0

    print(str(letras_acertadas) + "\n A palavra contem {} letras".format(len(letras_acertadas)))

    while (not enforcou and not acertou):

        chute = pede_chute()

        if (chute in palavra_secreta):
            marca_chute_correto(chute, letras_acertadas, palavra_secreta)
        else:
            erros += 1
            desenha_forca(erros)

        enforcou = erros == 7
        acertou = "_" not in letras_acertadas

        print(letras_acertadas)

    if(acertou):
        imprime_mensagem_vencedor()
    else:
        imprime_mensagem_perdedor(palavra_secreta)

    imprime_fim_jogo()

def imprime_boas_vindas():
    print("***********************************")
    print("* Olá bem vindo ao jogo de Forca! *")
    print("***********************************")

def imprime_fim_jogo():
    print("***********************************")
    print("******* Fim do Jogo! **************")
    print("***********************************")

def seleciona_palavra_secreta(nome_arquivo="palavras.txt"):
    palavras = []
    arquivo = open(nome_arquivo, "r")
    for linha in arquivo:
        palavras.append(linha.strip())
    arquivo.close()
    # random.randrange - retorna um numero aleatorio (rand) dentro de um intervalo (range) de opcoes.
    numero = random.randrange(0, len(palavras))
    return palavras[numero].upper()

def inicializa_palavras_acertadas(palavra):
    return ["_" for letra in palavra]
